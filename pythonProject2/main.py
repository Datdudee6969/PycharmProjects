grade = int(input('Enter Students grade:'))

if 90 <= grade <= 100:
    print("Your Grade is A")
elif 80 <= grade <= 89:
    print("Your Grade is B")
elif 70 <= grade <= 79:
    print("Your Grade is C")
elif 60 <= grade <= 69:
    print("Your Grade is D")
elif 50 <= grade <= 59:
    print("Your Grade is D")

else:
    print("Invalid Input!")
